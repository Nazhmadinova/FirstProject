package scanner;

import java.util.Scanner;

public class BecomesString {
    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);
        int n = input.nextInt();
        System.out.println(n + " + " + n + n + " + " + n + n + n); // input =5; output = 5 + 55 + 555;

    }
}